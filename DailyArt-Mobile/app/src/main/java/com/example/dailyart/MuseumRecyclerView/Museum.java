package com.example.dailyart.MuseumRecyclerView;

public class Museum {

    String image;
    String name;
    String city;
    Double latitude;
    Double longitude;

    public Museum() {}

    public String getImage() {
        return image;
    }

    public String getName() {
        return name;
    }

    public String getCity() {
        return city;
    }

    public Double getLatitude() { return latitude; }

    public Double getLongitude() { return longitude; }
}
